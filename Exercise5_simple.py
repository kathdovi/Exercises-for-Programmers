num1 = int(input("What is the first number? "))
num2 = int(input("What is the second number? "))
print("%d + %d = %d" % (num1, num2, num1 + num2))
print("%d - %d = %d" % (num1, num2, num1 - num2))
print("%d * %d = %d" % (num1, num2, num1 * num2))
print("%d / %d = %d" % (num1, num2, num1 / num2))
