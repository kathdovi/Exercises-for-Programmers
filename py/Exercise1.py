#Exercise 1: Saying Hello

#Ask user for name using input(), and include a prompt in quotation marks:
#Be sure to note the space after the ? and the last ".
#If you do not include this, there will be no space between the prompt and the user's response.
name = input("What is your name? ")

#Print the user's name within the sentence by using print():
#Note the use of %s, the string format operator. This allows you to substitute the var "name" where you place the %s.
print("Hello, %s, nice to meet you!" % name)

